using BH_ED
using Test

@testset "BH_ED.jl" begin
    r1 = BH_solver(N   = 6
                , Lp   = 3
                , L    = 3*6
                , μ    = 1.0
                , J    = 1.0
                , V    = 1.0
                , ε    = 1.0
    )
    t_cf_r = [2.1127883913424594,
    1.0465557381373054,
    1.3234461149460939,
    1.270276914830182,
    1.3234461149460939,
    1.0465557381373054,
    2.1127883913424594]

    @test abs(r1[1][1] + 14.60579110615412) <= 0.1
    @test abs(r1[3] - 6.813133331993588) <= 0.1
    @test abs( sum(r1[4] .- t_cf_r) ) <= 0.1
end
